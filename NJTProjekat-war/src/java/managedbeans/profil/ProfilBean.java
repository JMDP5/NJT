package managedbeans.profil;

import domen.Korisnik;
import java.util.List;
import javax.annotation.PostConstruct;
import javax.enterprise.context.RequestScoped;
import javax.faces.context.FacesContext;
import javax.inject.Named;
import javax.servlet.http.HttpSession;

/**
 *
 * @author aleksandar
 */
@Named(value = "profil")
@RequestScoped
public class ProfilBean {

    private Korisnik ulogovaniKorisnik;

    /**
     * Creates a new instance of ProfilBean
     */
    public ProfilBean() {
    }

    @PostConstruct
    public void init() {
        FacesContext context = FacesContext.getCurrentInstance();
        HttpSession session = (HttpSession) context.getExternalContext().getSession(false);
        ulogovaniKorisnik = (Korisnik) session.getAttribute("korisnik");
    }

    public Korisnik getUlogovaniKorisnik() {
        return ulogovaniKorisnik;
    }

    public void setUlogovaniKorisnik(Korisnik ulogovaniKorisnik) {
        this.ulogovaniKorisnik = ulogovaniKorisnik;
    }
    
    
}
